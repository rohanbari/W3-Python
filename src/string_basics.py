# All about strings goes here.

# Printing a huge string
big_string = """Hello, everyone! I am Rohan B.,
from Kolkata, India. I have a great enthusiasm
towards Computer Science."""
print(big_string)

# Strings are arrays
str_first = "Hello, world!"
print(str_first[4]) # Prints the 'o' letter

# Looping through a string
for elem in "banana":
    print(elem)

# String length
print(len(str_first))

# Checking if 'India' exists in big_string
print('India' in big_string)
# Note: 'not in' can be used to check whether something NOT exists.



