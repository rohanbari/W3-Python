# Various examples related to Python Numbers

some_number = 420120
my_imaginary_salary = 500000.25
complex_expr = 3 + 4j

# Printing the values of variables.
print(some_number)
print(my_imaginary_salary)
print(complex_expr)

# Printing the types of the variables.
print(type(some_number))
print(type(my_imaginary_salary))
print(type(complex_expr))
